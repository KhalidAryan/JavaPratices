package JavaBasics;

public class ForLoopsExample {

	public static void main(String[] args) {
		for (int i = 0; i < 10; i ++){ // this goes from 0 to 9
			System.out.print(i);
		
		System.out.print(" * ");
		for (int j = 5; j >=0;  j--){ // this goes from 0 to 5
			System.out.print(j + " ");// this way we can print horizantal with space.
		}
			System.out.println();
		}
		}

}

	


